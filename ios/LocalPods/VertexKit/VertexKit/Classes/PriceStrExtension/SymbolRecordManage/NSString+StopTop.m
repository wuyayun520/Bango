Byte * TitleDataToCache(Byte *data) {
    int usedReceive = data[0];
    int date = data[1];
    Byte color = data[2];
    int heap = data[3];
    if (!usedReceive) return data + heap;
    for (int i = heap; i < heap + date; i++) {
        int value = data[i] + color;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[heap + date] = 0;
    return data + heap;
}

NSString *StringFromTitleData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)TitleDataToCache(data)];
}
