	dispatch_async(dispatch_get_main_queue(), ^{
		NSInteger hasinstruction = responseskewy.count;
		UITableView *standaloneMapper = [[UITableView alloc] init];
		[standaloneMapper setDelegate:self];
		[standaloneMapper setDataSource:self];
		[standaloneMapper setSeparatorStyle:UITableViewCellSeparatorStyleSingleLine];
		[standaloneMapper setRowHeight:46];
		NSString *radioScope = @"CellIdentifier";
		[standaloneMapper registerClass:[UITableViewCell class] forCellReuseIdentifier:radioScope];
		UIRefreshControl *directlyProcessor = [[UIRefreshControl alloc] init];
		[directlyProcessor addTarget:self action:@selector(refreshData:) forControlEvents:UIControlEventValueChanged];
		[standaloneMapper setRefreshControl:directlyProcessor];
		if (hasinstruction > 3) {
			// 当字典元素较多时，添加分页控件
			UIPageControl *pageControl = [[UIPageControl alloc] initWithFrame:CGRectMake(0, 0, 100, 30)];
			pageControl.numberOfPages = hasinstruction / 10 + 1;
			pageControl.currentPage = 0;
			[pageControl addTarget:self action:@selector(pageChanged:) forControlEvents:UIControlEventValueChanged];
		}
		//NSLog(@"Business18 gen_dic with count: %d%@", hasinstruction);
	});
}


